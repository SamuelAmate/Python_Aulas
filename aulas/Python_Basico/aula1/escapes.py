#Utilizamos escapes para auxiliar na formatadação de exibição de dados, o caractere principal é a barra invertida \

# \n - pula uma linha (quebra de linha)
print("Hoje é um belo dia\nEstá ensolarado")

# \t - é o equivalente de dar um tab
print("Hoje é um belo dia\tEstá ensolarado")

# \v - é o equivalente de dar um tab vertical
print("Hoje é um belo dia\vEstá ensolarado")

# \r - é o equivalente de pressionar a tecla enter
print("Hoje é um belo dia\rEstá ensolarado")

# \' - serve para colocar aspas simples dentro de uma string que já está entre aspas simples
print('Hoje é um belo dia\'s ensolarado')

# \" - serve para colocar aspas duplas dentro de uma string que já está entre aspas duplas
print("Hoje é um belo dia\"s ensolarado")

# \\ - coloca uma barra invertida
print("Hoje é um belo dia \\ Está ensolarado")

# \a - emite um som (alerta)
print("Hoje é um belo dia\a Está ensolarado")

# \b - backspace (apaga o caractere anterior)
print("Hoje é um belo dia\b Está ensolarado")

# \f - quebra de página
print("Hoje é um belo dia\f Está ensolarado")

# \u - insere um caractere unicode (emoji)
print("Hoje é um belo dia \u263A Está ensolarado")